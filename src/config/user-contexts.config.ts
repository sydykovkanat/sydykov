/**
 * Персональные контексты для пользователей
 *
 * Здесь можно задать специфичный контекст для каждого пользователя,
 * чтобы AI общался с ним соответствующим образом.
 *
 * Ключ - это Telegram ID пользователя (можно узнать из логов или БД)
 * Значение - описание того, кто этот человек и как с ним общаться
 */

export const USER_CONTEXTS: Record<string, string> = {
  // Примеры:
  // '123456789': 'Это мой руководитель Асхат. Общайся с ним формально, уважительно, отчитывайся о прогрессе работы.',
  // '987654321': 'Это мой друг Тимур. Общайся неформально, можешь шутить и использовать сленг.',
  // '555555555': 'Это коллега из соседнего отдела. Общайся профессионально, но дружелюбно.',
};

/**
 * Получить персональный контекст для пользователя по его Telegram ID
 */
export function getUserContext(
  telegramId: string | bigint,
): string | undefined {
  const id = telegramId.toString();
  return USER_CONTEXTS[id];
}
